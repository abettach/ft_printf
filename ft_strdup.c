#include "ft_printf.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		i;
	int		j;

	j = 0;
	if (!s1 || !(str = malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
